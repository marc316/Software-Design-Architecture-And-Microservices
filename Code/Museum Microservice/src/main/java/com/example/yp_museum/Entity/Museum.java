package com.example.yp_museum.Entity;

import java.math.BigInteger;

public class Museum {
    private String museumName;
    private String altMuseumName;
    private BigInteger  museumPhone;
    private String museumURL;
    private String museumType;
    private String museumAddress;
    private String museumCity;
    private String museumState;
    private String museumZip;

    public String getMuseumName() {
        return museumName;
    }

    public void setMuseumName(String museumName) {
        this.museumName = museumName;
    }

    public String getAltMuseumName() {
        return altMuseumName;
    }

    public void setAltMuseumName(String altMuseumName) {
        this.altMuseumName = altMuseumName;
    }

    public BigInteger getMuseumPhone() {
        return museumPhone;
    }

    public void setMuseumPhone(BigInteger museumPhone) {
        this.museumPhone = museumPhone;
    }

    public String getMuseumURL() {
        return museumURL;
    }

    public void setMuseumURL(String museumURL) {
        this.museumURL = museumURL;
    }

    public String getMuseumType() {
        return museumType;
    }

    public void setMuseumType(String museumType) {
        this.museumType = museumType;
    }

    public String getMuseumAddress() {
        return museumAddress;
    }

    public void setMuseumAddress(String museumAddress) {
        this.museumAddress = museumAddress;
    }

    public String getMuseumCity() {
        return museumCity;
    }

    public void setMuseumCity(String museumCity) {
        this.museumCity = museumCity;
    }

    public String getMuseumState() {
        return museumState;
    }

    public void setMuseumState(String museumState) {
        this.museumState = museumState;
    }

    public String getMuseumZip() {
        return museumZip;
    }

    public void setMuseumZip(String museumZip) {
        this.museumZip = museumZip;
    }
}
